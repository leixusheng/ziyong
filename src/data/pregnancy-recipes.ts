// 初始化函数 - 将菜谱添加到localStorage
export function initializePregnancyRecipes() {
  const existingRecipes = JSON.parse(localStorage.getItem('meal-planner-recipes') || '[]')

  if (existingRecipes.length === 0) {
    const recipesWithIds = pregnancyRecipes.map((recipe, index) => ({
      ...recipe,
      id: `pregnancy-${index + 1}`,
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString(),
    }))

    localStorage.setItem('meal-planner-recipes', JSON.stringify(recipesWithIds))
    console.log(`已添加 ${recipesWithIds.length} 个孕妇营养菜谱`)
    return recipesWithIds.length
  }

  return 0
}
